from aiogram import types
from aiogram.types import KeyboardButton, ParseMode, ReplyKeyboardMarkup
from bot.controller.States import HelpMenu, StudentMenu, WaitAuth
from bot.model.User import User
from bot.repository.StateInfoRepository import StateInfoRepository
from bot.repository.UserRepository import UserRepository
from bot.service.TokenService import TokenService
from bot.teletrik.Controller import Controller
from bot.teletrik.DI import controller


@controller(HelpMenu)
class HelpMenuStateController(Controller):
    def __init__(
        self,
        user_repository: UserRepository,
        token_service: TokenService,
        state_info_repository: StateInfoRepository,
    ):
        self.user_repository: UserRepository = user_repository
        self.token_service: TokenService = token_service
        self.state_info_repository: StateInfoRepository = state_info_repository

    MAIN_MENU = "‚óÇ –ì–ª–∞–≤–Ω–æ–µ –º–µ–Ω—é"
    AUTH = "–°–º–µ–Ω–∏—Ç—å –∫–∞–±–∏–Ω–µ—Ç ‚ñ∏"
    REMEMBER = "–ù–∞–ø–æ–º–Ω–∏—Ç—å —Ç–æ–∫–µ–Ω"
    HOW_TO_SUBMIT = "‚ÑπÔ∏è –ö–∞–∫ —Å–¥–∞—Ç—å –∑–∞–¥–∞—á—É"
    HOW_TO_CHECK_RESULT = "‚ÑπÔ∏è –ö–∞–∫ —É–∑–Ω–∞—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç"
    HOW_TO_LEKTORIUM = "‚ÑπÔ∏è –ò–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –¥–ª—è –õ–µ–∫—Ç–æ—Ä–∏—É–º–∞"
    HOW_TO_UNDERSTAND_RESULT_SUBMIT = "‚ÑπÔ∏è –ß—Ç–æ –æ–∑–Ω–∞—á–∞—é—Ç + - ?"
    HOW_TO_UNDERSTAND_RESULT_TASK = "‚ÑπÔ∏è –ß—Ç–æ –æ–∑–Ω–∞—á–∞—é—Ç ‚úÖ ‚ùå üîÑ"

    CHOOSE_ACTION = "–í—ã–±–µ—Ä–∏—Ç–µ –¥–µ–π—Å—Ç–≤–∏–µ, –Ω–∞–∂–∞–≤ –Ω—É–∂–Ω—É—é –∫–Ω–æ–ø–∫—É"

    HELP_MENU_KEYBOARD = ReplyKeyboardMarkup()
    HELP_MENU_KEYBOARD.add(
        KeyboardButton(MAIN_MENU),
        KeyboardButton(AUTH),
        KeyboardButton(REMEMBER),
        KeyboardButton(HOW_TO_SUBMIT),
        KeyboardButton(HOW_TO_CHECK_RESULT),
        KeyboardButton(HOW_TO_LEKTORIUM),
        KeyboardButton(HOW_TO_UNDERSTAND_RESULT_SUBMIT),
        KeyboardButton(HOW_TO_UNDERSTAND_RESULT_TASK),
    )

    async def handle(self, message: types.Message):

        match message.text:

            case self.AUTH:
                return WaitAuth

            case self.MAIN_MENU:
                tg_id: str = message.from_user.id
                user: User | None = await self.user_repository.get_by_telegram_id(tg_id)

                if user is None:
                    token: str = self.token_service.generate_new_token(tg_id)
                    await self.user_repository.create_user(token, "student", tg_id)
                    self.state_info_repository.create(
                        message.from_user.id, token
                    )
                    return StudentMenu
                else:
                    self.state_info_repository.create(
                        message.from_user.id, user.user_id
                    )
                    return StudentMenu

            case self.REMEMBER:
                tg_id: str = message.from_user.id
                user: User | None = await self.user_repository.get_by_telegram_id(tg_id)

                if user is None:
                    await message.answer(
                        "–í—ã –µ—â—ë –Ω–µ –∑–∞—Ä–µ–≥–µ—Å—Ç—Ä–∏—Ä–æ–≤–∞–Ω—ã, –¥–ª—è —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏ –æ—Ç–ø—Ä–∞–≤—å—Ç–µ /start"
                    )
                else:
                    await message.answer("–í–∞—à —Ç–æ–∫–µ–Ω:")
                    await message.answer(user.user_id)
                return HelpMenu

            case self.HOW_TO_SUBMIT:
                await message.answer(
                    """*–ö–∞–∫ —Å–¥–∞—Ç—å –∑–∞–¥–∞—á—É?*
1. –í –ø–æ—è–≤–∏–≤—à–µ–º—Å—è –ø–æ—Å–ª–µ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ –º–µ–Ω—é –Ω–∞–∂–º–∏—Ç–µ –Ω–∞ –∫–Ω–æ–ø–∫—É —Å –∑–∞–¥–∞—á–µ–π, —Ä–µ—à–µ–Ω–∏–µ –Ω–∞ –∫–æ—Ç–æ—Ä—É—é —Å–æ–±–∏—Ä–∞–µ—Ç–µ—Å—å –æ—Ç–ø—Ä–∞–≤–∏—Ç—å.
2. –ù–∞–∂–º–∏—Ç–µ –Ω–∞ `–û—Ç–ø—Ä–∞–≤–∏—Ç—å‚ñ∏`.
3. –ü—Ä–∏–∫—Ä–µ–ø–∏—Ç–µ —Ñ–∞–π–ª —Å —Ä–µ—à–µ–Ω–∏–µ–º –∏ –æ—Ç–ø—Ä–∞–≤—å—Ç–µ –µ–≥–æ –±–æ—Ç—É.
4. –í –æ—Ç–≤–µ—Ç –≤—ã –ø–æ–ª—É—á–∏—Ç–µ id –≤–∞—à–µ–≥–æ —Ä–µ—à–µ–Ω–∏—è, –ø–æ –∫–æ—Ç–æ—Ä–æ–º—É –≤—ã —Å–º–æ–∂–µ—Ç–µ —É–∑–Ω–∞—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç.""",
                    parse_mode=ParseMode.MARKDOWN,
                )
                return HelpMenu
            case self.HOW_TO_CHECK_RESULT:
                await message.answer(
                    """*–ö–∞–∫ —É–∑–Ω–∞—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç?*
1. –í –ø–æ—è–≤–∏–≤—à–µ–º—Å—è –ø–æ—Å–ª–µ –∞–≤—Ç–æ—Ä–∏–∑–∞—Ü–∏–∏ –º–µ–Ω—é –≤—ã –º–æ–∂–µ—Ç–µ —É–≤–∏–¥–µ—Ç—å –∏—Ç–æ–≥–æ–≤—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç –ø–æ –∫–∞–∂–¥–æ–π –∑–∞–¥–∞—á–µ.
2. –ï—Å–ª–∏ –≤—ã —Ö–æ—Ç–∏—Ç–µ —É–∑–Ω–∞—Ç—å —Ä–µ–∑—É–ª—å—Ç–∞—Ç –ø–æ –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–º—É —Ä–µ—à–µ–Ω–∏—é, —Ç–æ –Ω–∞–∂–º–∏—Ç–µ –Ω–∞ –∫–Ω–æ–ø–∫—É —Å —Å–æ–æ—Ç–≤–µ—Ç—Å–≤—É—é—â–µ–π –∑–∞–¥–∞—á–µ–π.
3. –ù–∞–∂–º–∏—Ç–µ –Ω–∞ `–ü–æ–ø—ã—Ç–∫–∏`.
4. –í –æ—Ç–≤–µ—Ç –≤—ã –ø–æ–ª—É—á–∏—Ç–µ —Ç–∞–±–ª–∏—Ü—É —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –ø—Ä–æ–≤–µ—Ä–∫–∏ –ø–æ –∫–∞–∂–¥–æ–º—É –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–Ω–æ–º—É —Ä–µ—à–µ–Ω–∏—é.""",
                    parse_mode=ParseMode.MARKDOWN,
                )
                return HelpMenu
            case self.HOW_TO_LEKTORIUM:
                await message.answer(
                    """*–ö–∞–∫ —É–∑–Ω–∞—Ç—å –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –¥–ª—è –õ–µ–∫—Ç–æ—Ä–∏—É–º–∞?*
1. –ù–∞–∂–º–∏—Ç–µ –Ω–∞ –∫–Ω–æ–ø–∫—É —Å –∑–∞–¥–∞—á–µ–π, –∑–∞ –∫–æ—Ç–æ—Ä—É—é —É –≤–∞—Å —Å—Ç–æ–∏—Ç ‚úÖ.
2. –ù–∞–∂–º–∏—Ç–µ –Ω–∞ –∫–Ω–æ–ø–∫—É `–î–∞–Ω–Ω—ã–µ –¥–ª—è –õ–µ–∫—Ç–æ–∏—É–º–∞`. –ë–æ—Ç –≤–∞–º –æ—Ç–≤–µ—Ç–∏—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ–º, –∫–æ—Ç–æ—Ä–æ–µ —Å–æ–¥–µ—Ä–∂–∏—Ç "hash" –∏ "pin".
3. –°–∫–æ–ø–∏—Ä—É–π—Ç–µ "hash" –∏ "pin" –∏ –≤–≤–µ–¥–∏—Ç–µ –≤ —Å–æ–æ—Ç–≤–µ—Ç—Å–≤—É—é—â–∏–µ –ø–æ–ª—è –Ω–∞ —Å–∞–π—Ç–µ –õ–µ–∫—Ç–æ—Ä–∏—É–º–∞.""",
                    parse_mode=ParseMode.MARKDOWN,
                )
                return HelpMenu
            case self.HOW_TO_UNDERSTAND_RESULT_SUBMIT:
                await message.answer(
                    """*–°—Ç–∞—Ç—É—Å—ã —Ä–µ—à–µ–Ω–∏–π*
‚ö¨ `+` ‚Äî —Ä–µ—à–µ–Ω–∏–µ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–µ.
‚ö¨ `-` ‚Äî —Ä–µ—à–µ–Ω–∏–µ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω–æ–µ.
‚ö¨ `?` ‚Äî —Ä–µ—à–µ–Ω–∏–µ —Ç–µ—Å—Ç–∏—Ä—É–µ—Ç—Å—è.""",
                    parse_mode=ParseMode.MARKDOWN,
                )
                return HelpMenu
            case self.HOW_TO_UNDERSTAND_RESULT_TASK:
                await message.answer(
                    """*–°—Ç–∞—Ç—É—Å—ã –∑–∞–¥–∞—á*
–ï—Å–ª–∏ –±—ã–ª–æ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ —Ö–æ—Ç—è –±—ã –æ–¥–Ω–æ —Ä–µ—à–µ–Ω–∏–µ, –≤—ã —Å–º–æ–∂–µ—Ç–µ —É–≤–∏–¥–µ—Ç—å –æ–¥–∏–Ω –∏–∑ —Å–ª–µ–¥—É—é—â–∏—Ö —Å—Ç–∞—Ç—É—Å–æ–≤:
‚ö¨ ‚úÖ ‚Äî –µ—Å–ª–∏ –±—ã–ª–æ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ —Ö–æ—Ç—è –±—ã –æ–¥–Ω–æ –ø—Ä–∞–≤–∏–ª—å–Ω–æ–µ —Ä–µ—à–µ–Ω–∏–µ.
‚ö¨ ‚ùå ‚Äî –µ—Å–ª–∏ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö —Ä–µ—à–µ–Ω–∏–π –Ω–µ—Ç.
‚ö¨ üîÑ ‚Äî –µ—Å–ª–∏ –ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö —Ä–µ—à–µ–Ω–∏–π –Ω–µ—Ç, –Ω–æ –µ—Å—Ç—å —Ä–µ—à–µ–Ω–∏—è, –∫–æ—Ç–æ—Ä—ã–µ —Ç–µ—Å—Ç–∏—Ä—É—é—Ç—Å—è.""",
                    parse_mode=ParseMode.MARKDOWN,
                )
                return HelpMenu
            case _:
                await message.answer(
                    "–Ø –í–∞—Å –Ω–µ –ø–æ–Ω—è–ª, –ø–æ–∂–∞–ª—É–π—Å—Ç–∞ –≤–æ—Å–ø–æ–ª—å–∑—É–π—Ç–µ—Å—å –∫–Ω–æ–ø–∫–æ–π –∏–∑ –∫–ª–∞–≤–∏–∞—Ç—É—Ä—ã"
                )
                return HelpMenu

    async def prepare(self, message: types.Message):
        await message.answer(self.CHOOSE_ACTION, reply_markup=self.HELP_MENU_KEYBOARD)
